type perfHTMLData struct {
	TestName   string
	ConfigName string
	Rows       []perfHTMLRow
}

type perfHTMLRow struct {
	Subject         string
	Version         string
	ThroughputFmt   string
	BarPct          float64
	AvgCPU          float64
	MaxCPU          float64
	AvgMemMB        float64
	MaxMemMB        float64
	TotalNetMB      float64
	DiskReadMB      float64
	DiskWriteMB     float64
	IOThroughputFmt string
	LoadAvg1        float64
}

type correctnessHTMLData struct {
	TestName   string
	ConfigName string
	Rows       []correctnessHTMLRow
}

type correctnessHTMLRow struct {
	Subject     string
	Version     string
	Result      string
	ResultClass string
	LinesOutFmt string
	DurationS   float64
	Details     string
}

func writeHTMLPerformance(w io.Writer, testName, configName string, ss []SubjectSummary) error {
	// Find max throughput for bar chart scaling
	var maxTP float64
	for _, s := range ss {
		if s.Throughput > maxTP {
			maxTP = s.Throughput
		}
	}

	data := perfHTMLData{TestName: testName, ConfigName: configName}
	for _, s := range ss {
		barPct := 0.0
		if maxTP > 0 {
			barPct = (s.Throughput / maxTP) * 100
		}
		data.Rows = append(data.Rows, perfHTMLRow{
			Subject:         s.Subject,
			Version:         s.Version,
			ThroughputFmt:   formatInt(int64(s.Throughput)),
			BarPct:          barPct,
			AvgCPU:          s.AvgCPU,
			MaxCPU:          s.MaxCPU,
			AvgMemMB:        s.AvgMemMB,
			MaxMemMB:        s.MaxMemMB,
			TotalNetMB:      s.TotalNetMB,
			DiskReadMB:      s.DiskReadMB,
			DiskWriteMB:     s.DiskWriteMB,
			IOThroughputFmt: formatBytes(s.IOThroughputAvg),
			LoadAvg1:        s.LoadAvg1,
		})
	}

	tmpl, err := template.New("perf").Parse(htmlPerformanceTemplate)
	if err != nil {
		return fmt.Errorf("parsing html template: %w", err)
	}
	return tmpl.Execute(w, data)
}

func writeHTMLCorrectness(w io.Writer, testName, configName string, ss []SubjectSummary) error {
	data := correctnessHTMLData{TestName: testName, ConfigName: configName}
	for _, s := range ss {
		result := "?"
		resultClass := "unknown"
		if s.Passed != nil {
			if *s.Passed {
				result = "PASS"
				resultClass = "pass"
			} else {
				result = "FAIL"
				resultClass = "fail"
			}
		}
		details := "-"
		if s.FailReason != "" {
			details = s.FailReason
		}
		data.Rows = append(data.Rows, correctnessHTMLRow{
			Subject:     s.Subject,
			Version:     s.Version,
			Result:      result,
			ResultClass: resultClass,
			LinesOutFmt: formatInt(s.LinesOut),
			DurationS:   s.DurationS,
			Details:     details,
		})
	}

	tmpl, err := template.New("correctness").Parse(htmlCorrectnessTemplate)
	if err != nil {
		return fmt.Errorf("parsing html template: %w", err)
	}
	return tmpl.Execute(w, data)
}
