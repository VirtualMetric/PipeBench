package orchestrator

import (
	"fmt"
	"os"
	"strings"

	"github.com/VirtualMetric/PipeBench/internal/config"
)

// writeAWSInit renders the LocalStack init script for a case's `aws:` block
// and writes it to path (mode 0755 — LocalStack executes ready.d hooks).
// The script runs inside the LocalStack container after the emulator is up
// and before its init state reports "completed", so the compose healthcheck
// gates the whole topology on these resources existing.
//
// Every name interpolated here passed config.Validate()'s charset check
// ([a-zA-Z0-9._/-]), so single-quoting is belt-and-braces, not the only
// injection defense.
func writeAWSInit(path string, aws *config.AWSConfig) error {
	var b strings.Builder
	b.WriteString("#!/bin/sh\n")
	b.WriteString("# Auto-generated by harness — creates bench resources inside LocalStack.\n")
	b.WriteString("set -eu\n")

	for _, bucket := range aws.Buckets {
		fmt.Fprintf(&b, "awslocal s3 mb 's3://%s'\n", bucket)
	}
	for _, queue := range aws.Queues {
		fmt.Fprintf(&b, "awslocal sqs create-queue --queue-name '%s'\n", queue)
	}
	for _, topic := range aws.Topics {
		fmt.Fprintf(&b, "awslocal sns create-topic --name '%s'\n", topic)
	}
	for _, stream := range aws.Streams {
		shards := stream.Shards
		if shards == 0 {
			shards = 1
		}
		fmt.Fprintf(&b, "awslocal kinesis create-stream --stream-name '%s' --shard-count %d\n", stream.Name, shards)
		fmt.Fprintf(&b, "awslocal kinesis wait stream-exists --stream-name '%s'\n", stream.Name)
	}
	for _, lg := range aws.LogGroups {
		fmt.Fprintf(&b, "awslocal logs create-log-group --log-group-name '%s'\n", lg.Group)
		if lg.Stream != "" {
			fmt.Fprintf(&b, "awslocal logs create-log-stream --log-group-name '%s' --log-stream-name '%s'\n", lg.Group, lg.Stream)
		}
	}
	for _, n := range aws.BucketNotifications {
		fmt.Fprintf(&b,
			"awslocal s3api put-bucket-notification-configuration --bucket '%s' --notification-configuration '{\"QueueConfigurations\":[{\"QueueArn\":\"%s\",\"Events\":[\"s3:ObjectCreated:*\"]}]}'\n",
			n.Bucket, aws.QueueARN(n.Queue))
	}
	for _, s := range aws.Subscriptions {
		fmt.Fprintf(&b,
			"awslocal sns subscribe --topic-arn '%s' --protocol sqs --notification-endpoint '%s' --attributes RawMessageDelivery=true\n",
			aws.TopicARN(s.Topic), aws.QueueARN(s.Queue))
	}

	b.WriteString("echo 'pipebench aws init complete'\n")
	return os.WriteFile(path, []byte(b.String()), 0o755)
}

// cloudEnvForMode returns the emulator env a cloud generator/receiver mode
// needs (nil for non-cloud modes, so existing cases render unchanged).
func cloudEnvForMode(mode string, awsEnv, azureEnv map[string]string) map[string]string {
	switch mode {
	case "s3", "sqs", "kinesis", "cloudwatch":
		return awsEnv
	case "azure_blob":
		return azureEnv
	}
	return nil
}

// mergeEnv merges maps left to right (later keys win), returning nil when
// nothing was set so template blocks gated on the map stay omitted.
func mergeEnv(maps ...map[string]string) map[string]string {
	var out map[string]string
	for _, m := range maps {
		for k, v := range m {
			if out == nil {
				out = map[string]string{}
			}
			out[k] = v
		}
	}
	return out
}
