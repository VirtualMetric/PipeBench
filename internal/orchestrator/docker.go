// RunConfig holds parameters for a single test run.
type RunConfig struct {
	TestCase         *config.TestCase
	Subject          config.Subject
	ConfigName       string
	ConfigSrcPath    string // absolute path to the subject config file on host
	TmpDir           string // temp dir for this run
	GeneratorImage   string
	ReceiverImage    string
	CollectorImage   string
	ReceiverHostPort int
	ExtraSubjectEnv  map[string]string
	CPULimit         string // e.g. "1", "4" — number of cores for subject
	MemLimit         string // e.g. "1g", "16g" — memory limit for subject
	DockerSocketGID  string // numeric gid owning /var/run/docker.sock; passed to collector via group_add so a non-root image can still read the socket
}

// ComposeRunner manages a docker compose lifecycle for one test run.
type ComposeRunner struct {
	cfg         RunConfig
	composeFile string
}

// Ensure ComposeRunner implements Orchestrator.
var _ Orchestrator = (*ComposeRunner)(nil)

// NewComposeRunner creates a ComposeRunner and writes the compose file.
func NewComposeRunner(cfg RunConfig) (*ComposeRunner, error) {
	if err := os.MkdirAll(cfg.TmpDir, 0o755); err != nil {
		return nil, err
	}

	if cfg.DockerSocketGID == "" {
		cfg.DockerSocketGID = dockerSocketGID()
	}

	composeFile := filepath.Join(cfg.TmpDir, "docker-compose.yaml")
	if err := writeCompose(composeFile, cfg); err != nil {
		return nil, err
	}

	return &ComposeRunner{cfg: cfg, composeFile: composeFile}, nil
}

// dockerSocketGID is defined per-platform (docker_socket_gid_*.go). It returns
// the numeric gid owning /var/run/docker.sock, or "" if it can't be determined
// (e.g. Windows/macOS Docker Desktop). The collector image runs as a hardened
// non-root uid and needs to be in the docker group to read the socket over its
// HTTP API — the value is passed to docker-compose via `group_add`.

// Up starts all services detached.
func (r *ComposeRunner) Up() error {
	return r.compose("up", "-d", "--quiet-pull")
}

// UpServices starts only the named compose services.
func (r *ComposeRunner) UpServices(services ...string) error {
	args := append([]string{"up", "-d", "--quiet-pull"}, services...)
	return r.compose(args...)
}

// StopServices sends SIGTERM to named services with the given grace timeout
// before SIGKILL. Uses `docker compose stop -t <seconds> <svc>...`.
func (r *ComposeRunner) StopServices(timeout time.Duration, services ...string) error {
	secs := int(timeout.Seconds())
	if secs < 0 {
		secs = 0
	}
	args := append([]string{"stop", "-t", strconv.Itoa(secs)}, services...)
	return r.compose(args...)
}

// Down stops and removes all containers and anonymous volumes.
func (r *ComposeRunner) Down() error {
	return r.compose("down", "-v", "--remove-orphans")
}

// WaitForGeneratorExit blocks until the generator container exits or timeout expires.
// Returns an error if the generator exited with a non-zero status.
func (r *ComposeRunner) WaitForGeneratorExit(timeout time.Duration) error {
	deadline := time.Now().Add(timeout)
	for time.Now().Before(deadline) {
		out, err := exec.Command("docker", "inspect", "--format={{.State.Status}}", "bench-generator").Output()
		if err == nil {
			status := strings.TrimSpace(string(out))
			if status == "exited" {
				return r.checkGeneratorExitCode()
			}
		}
		time.Sleep(2 * time.Second)
	}
	return fmt.Errorf("generator did not exit within %s", timeout)
}

func (r *ComposeRunner) checkGeneratorExitCode() error {
	out, err := exec.Command("docker", "inspect", "--format={{.State.ExitCode}}", "bench-generator").Output()
	if err != nil {
		return nil // can't check, assume OK
	}
	code := strings.TrimSpace(string(out))
	if code != "0" {
		// Grab the generator logs so the user can see what went wrong
		logs, _ := exec.Command("docker", "logs", "--tail", "50", "bench-generator").CombinedOutput()
		return fmt.Errorf("generator exited with code %s:\n%s", code, string(logs))
	}
	return nil
}

// Logs returns the last N lines of logs for a compose service.
func (r *ComposeRunner) Logs(service string, lines int) string {
	out, err := exec.Command("docker", "compose", "-f", r.composeFile,
		"logs", "--tail", fmt.Sprintf("%d", lines), "--no-color", service).CombinedOutput()
	if err != nil {
		return fmt.Sprintf("(could not get logs: %v)", err)
	}
	return string(out)
}

// StopCollector sends SIGTERM to the collector container and waits for it to exit.
// The collector writes its CSV file on SIGTERM, so this must be called before CopyMetricsCSV.
func (r *ComposeRunner) StopCollector() error {
	// docker stop sends SIGTERM and waits up to 10s for graceful exit.
	out, err := exec.Command("docker", "stop", "-t", "10", "bench-collector").CombinedOutput()
	if err != nil {
		return fmt.Errorf("docker stop bench-collector: %w\n%s", err, out)
	}
	return nil
}

// CopyMetricsCSV copies the metrics CSV from the (stopped) collector container to dst.
// Call StopCollector first so the CSV has been flushed.
func (r *ComposeRunner) CopyMetricsCSV(dst string) error {
	src := "bench-collector:/results/metrics.csv"
	out, err := exec.Command("docker", "cp", src, dst).CombinedOutput()
	if err == nil {
		return nil
	}
	// Enrich the error when the collector crashed before writing — otherwise
	// the user just sees "Could not find the file" with no clue why.
	stateOut, _ := exec.Command("docker", "inspect",
		"--format={{.State.Status}}|{{.State.ExitCode}}|{{.State.Error}}",
		"bench-collector").Output()
	state := strings.TrimSpace(string(stateOut))
	if state != "" && !strings.HasPrefix(state, "running|") {
		logs, _ := exec.Command("docker", "logs", "--tail", "30", "bench-collector").CombinedOutput()
		return fmt.Errorf("docker cp %s → %s: %w\ncollector state: %s\ncollector logs:\n%s\ndocker cp stderr:\n%s",
			src, dst, err, state, string(logs), out)
	}
	return fmt.Errorf("docker cp %s → %s: %w\n%s", src, dst, err, out)
}

// SubjectContainer returns the container name used for the subject.
func (r *ComposeRunner) SubjectContainer() string {
	return "bench-subject-" + r.cfg.Subject.Name
}

// ReceiverMetricsPort returns the host port where the receiver's /metrics
// endpoint is already exposed via the compose port mapping.
func (r *ComposeRunner) ReceiverMetricsPort() (int, func(), error) {
	return r.cfg.ReceiverHostPort, func() {}, nil
}

// GeneratorStdout returns the stdout of the generator container.
func (r *ComposeRunner) GeneratorStdout() string {
	out, _ := exec.Command("docker", "logs", "bench-generator").Output()
	return string(out)
}

func (r *ComposeRunner) compose(args ...string) error {
	cmdArgs := append([]string{"compose", "-f", r.composeFile}, args...)
	cmd := exec.Command("docker", cmdArgs...)
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	return cmd.Run()
}

type composeVars struct {
	SubjectImage      string
	SubjectContainer  string
	ConfigSrc         string
	ConfigDst         string
	ConfigMountOpts   string // ":ro" or ""
	TmpDir            string
	SubjectCmd        string
	SubjectEntrypoint string
	SubjectUser       string
	SubjectEnv        map[string]string
	HasResourceLimits bool
	CPULimit          string
	MemLimit          string
	UseSharedData     bool
	DeferReceiver     bool
	GeneratorImage    string
	ReceiverImage     string
	CollectorImage    string
	ReceiverHostPort  int
	GenMode           string
	GenTarget         string
	GenRate           int
	GenDuration       string
	GenLineSize       int
	GenFormat         string
	GenWarmup         string
	GenSequenced      string
	GenConnections    int
	GenTotalLines     int64
	GenEnv            map[string]string
	RecvMode              string
	RecvListen            string
	RecvValidateOrder     string
	RecvValidateDedup     string
	RecvValidateContent   string
	RecvExpectedLines     int64
	RecvRequiredSubstring string
	DockerSocketGID       string
}

func writeCompose(path string, cfg RunConfig) error {
	tc := cfg.TestCase
	s := cfg.Subject

	// Docker Compose on Windows needs forward slashes in volume paths;
	// backslashes are interpreted as YAML escape sequences inside quoted strings.
	configSrc := filepath.ToSlash(cfg.ConfigSrcPath)
	tmpDir := filepath.ToSlash(cfg.TmpDir)

	subjectContainer := "bench-subject-" + s.Name

	// Merge subject default env + extra env from configuration
	env := map[string]string{}
	for k, v := range s.Env {
		env[k] = v
	}
	for k, v := range cfg.ExtraSubjectEnv {
		env[k] = v
	}

	warmup := tc.WarmupOrDefault(10 * time.Second).String()
	duration := tc.DurationOrDefault(2 * time.Minute).String()

	genLineSize := tc.Generator.LineSize
	if genLineSize == 0 {
		genLineSize = 256
	}
	genFormat := tc.Generator.Format
	if genFormat == "" {
		genFormat = "raw"
	}

	// File-based tests need a shared volume between generator and subject.
	useSharedData := tc.Generator.Mode == "file"

	// When config source is a directory but ConfigPath points to a file,
	// mount to the parent directory so all files in the config dir are available.
	configDst := s.ConfigPath
	configIsDir := false
	if info, err := os.Stat(cfg.ConfigSrcPath); err == nil && info.IsDir() {
		configIsDir = true
		if ext := filepath.Ext(configDst); ext != "" {
			configDst = filepath.ToSlash(filepath.Dir(configDst))
		}
	}

	// For logstash with directory config, mount to config dir
	// (logstash.yml in that dir sets path.config to find the pipeline)
	if s.Name == "logstash" && configIsDir {
		configDst = "/usr/share/logstash/config"
	}

	vars := composeVars{
		SubjectImage:      s.ImageRef(),
		SubjectContainer:  subjectContainer,
		ConfigSrc:         configSrc,
		ConfigDst:         configDst,
		ConfigMountOpts:   configMountOpts(s),
		TmpDir:            tmpDir,
		SubjectCmd:        formatYAMLList(s.Command),
		SubjectEntrypoint: formatYAMLList(s.Entrypoint),
		SubjectUser:       s.User,
		SubjectEnv:        env,
		HasResourceLimits: cfg.CPULimit != "" || cfg.MemLimit != "",
		CPULimit:          defaultStr(cfg.CPULimit, "1"),
		MemLimit:          defaultStr(cfg.MemLimit, "1g"),
		UseSharedData:     useSharedData,
		DeferReceiver:     tc.Type == "persistence_correctness",
		GeneratorImage:    cfg.GeneratorImage,
		ReceiverImage:     cfg.ReceiverImage,
		CollectorImage:    cfg.CollectorImage,
		ReceiverHostPort:  cfg.ReceiverHostPort,
		GenMode:           tc.Generator.Mode,
		GenTarget:         tc.Generator.Target,
		GenRate:           tc.Generator.Rate,
		GenDuration:       duration,
		GenLineSize:       genLineSize,
		GenFormat:         genFormat,
		GenWarmup:         warmup,
		GenSequenced:      boolStr(tc.Type == "correctness" || tc.Type == "persistence_correctness" || tc.Type == "persistence_restart_correctness" || tc.Correctness.ValidateDedup || tc.Correctness.ValidateContent),
		GenConnections:    resolveGeneratorConnections(tc.Generator.Connections),
		GenTotalLines:     tc.Generator.TotalLines,
		GenEnv:            tc.Generator.Env,
		RecvMode:              tc.Receiver.Mode,
		RecvListen:            tc.Receiver.Listen,
		RecvValidateOrder:     boolStr(tc.Correctness.ValidateOrder),
		RecvValidateDedup:     boolStr(tc.Correctness.ValidateDedup),
		RecvValidateContent:   boolStr(tc.Correctness.ValidateContent),
		RecvExpectedLines:     0,
		RecvRequiredSubstring: tc.Correctness.RequiredSubstring,
		DockerSocketGID:       cfg.DockerSocketGID,
	}

	tmpl, err := template.New("compose").Parse(composeTemplate)
	if err != nil {
		return err
	}

	f, err := os.Create(path)
	if err != nil {
		return err
	}
	defer f.Close()

	return tmpl.Execute(f, vars)
}

// formatYAMLList formats a string slice as a YAML inline list: ["a", "b", "c"]
func formatYAMLList(items []string) string {
	if len(items) == 0 {
		return ""
	}
	quoted := make([]string, len(items))
	for i, s := range items {
		quoted[i] = fmt.Sprintf("%q", s)
	}
	return "[" + strings.Join(quoted, ", ") + "]"
}

func configMountOpts(s config.Subject) string {
	if s.ConfigRW {
		return ""
	}
	return ":ro"
}

func defaultStr(val, def string) string {
	if val != "" {
		return val
	}
	return def
}

func boolStr(b bool) string {
	if b {
		return "true"
	}
	return "false"
}
