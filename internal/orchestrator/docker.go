// resolveGeneratorConnections picks the generator's parallel-connection
// count. An explicit non-zero value in case.yaml always wins. When unset
// (0), scale with the host's CPU count at 3× cores — enough parallelism
// to drive the subject's worker pools past their per-worker steady state
// without leaving large fractions of a multi-core box idle. On a 4-core
// dev box this produces 12 connections; on a 32-core box, 96; on a
// 192-core c6a.metal, 576.
//
// The intent is "what the host can drive", not "one connection per CPU" —
// the bench's subjects (vmetric/vector/etc.) typically run their own
// per-listener worker pools sized to NumCPU, so feeding them ~3× more
// connections than cores keeps those pools saturated rather than starved.
func resolveGeneratorConnections(explicit int) int {
	if explicit > 0 {
		return explicit
	}
	if n := runtime.NumCPU() * 3; n > 0 {
		return n
	}
	return 3
}

// configTemplateContext is the data exposed to a subject config rendered as a
// Go text/template. It carries only generic facts about the run — never a
// subject's tuning vocabulary — so a config adapts itself to the host
// (e.g. an OpenTelemetry source sets workers() to the CPU count).
type configTemplateContext struct {
	// CPUs is the host CPU count.
	CPUs int
}

// renderSubjectConfig renders the subject config at srcPath as a Go
// text/template when it contains template actions ("{{"), writing the result
// into tmpDir and returning the rendered file's path. Configs with no template
// actions, directory-form configs (e.g. cribl-stream), and missing paths are
// returned unchanged — so non-templated subjects, and the existing
// missing-path tolerance, are unaffected.
func renderSubjectConfig(srcPath, tmpDir string, ctx configTemplateContext) (string, error) {
	info, err := os.Stat(srcPath)
	if err != nil || info.IsDir() {
		return srcPath, nil
	}
	raw, err := os.ReadFile(srcPath)
	if err != nil {
		return "", err
	}
	if !strings.Contains(string(raw), "{{") {
		return srcPath, nil
	}
	tmpl, err := template.New(filepath.Base(srcPath)).Parse(string(raw))
	if err != nil {
		return "", fmt.Errorf("parsing config template %s: %w", srcPath, err)
	}
	outPath := filepath.Join(tmpDir, filepath.Base(srcPath))
	f, err := os.Create(outPath)
	if err != nil {
		return "", err
	}
	defer f.Close()
	if err := tmpl.Execute(f, ctx); err != nil {
		return "", fmt.Errorf("rendering config template %s: %w", srcPath, err)
	}
	return outPath, nil
}

