// RunConfig holds parameters for a single test run.
type RunConfig struct {
	TestCase         *config.TestCase
	Subject          config.Subject
	ConfigName       string
	ConfigSrcPath    string // absolute path to the subject config file on host
	CaseDir          string // absolute path to the case directory on host (locates generator sample_file, etc.)
	TmpDir           string // temp dir for this run
	GeneratorImage   string
	ReceiverImage    string
	CollectorImage   string
	VerifierImage    string // DuckDB verifier image (verifier cases only)
	ReceiverHostPort int    // base host port; in multi-receiver mode, +N for receiver N
	ExtraSubjectEnv  map[string]string
	CPULimit         string // e.g. "1", "4" — number of cores for subject
	MemLimit         string // e.g. "1g", "16g" — memory limit for subject
	DockerSocketGID  string // numeric gid owning /var/run/docker.sock; passed to collector via group_add so a non-root image can still read the socket
	// TLSCertsHost, when non-empty, is the absolute host path holding
	// ca.crt + server.crt/key + client.crt/key for a TLS-enabled run.
	// The harness writes the certs there before `docker compose up`; the
	// path is bind-mounted into the subject and every TLS-using generator
	// at /certs:ro.
	TLSCertsHost string
	// VaultTLSHost / VaultSecretsHost / VaultSeeds are the host paths and
	// seed list provisioned by PrepareVault for a `vault:`-enabled case.
	// NewComposeRunner populates them when empty; tests may set them
	// directly to render a compose file without touching the filesystem.
	VaultTLSHost     string
	VaultSecretsHost string
	VaultSeeds       []VaultSeed
	// KrbHostDir / KerberosInitCmd are provisioned by PrepareKerberos for a
	// `kafka.auth.mechanism: gssapi` case. NewComposeRunner populates them when
	// empty; tests may set them directly to render without touching the KDC.
	KrbHostDir      string
	KerberosInitCmd string
}

// ComposeRunner manages a docker compose lifecycle for one test run.
type ComposeRunner struct {
	cfg         RunConfig
	composeFile string

	// genServices / recvServices cache the compose service names so the
	// runner can stop/inspect them without re-deriving from the case.
	genServices    []string
	genContainers  []string
	recvServices   []string
	recvContainers []string
	// recvHostPorts maps receiver id → host port for the /metrics endpoint.
	recvHostPorts map[string]int
}

// Ensure ComposeRunner implements Orchestrator.
var _ Orchestrator = (*ComposeRunner)(nil)

// NewComposeRunner creates a ComposeRunner and writes the compose file.
func NewComposeRunner(cfg RunConfig) (*ComposeRunner, error) {
	if err := os.MkdirAll(cfg.TmpDir, 0o755); err != nil {
		return nil, err
	}

	if cfg.DockerSocketGID == "" {
		cfg.DockerSocketGID = dockerSocketGID()
	}

	// Vault topology prep lives here (not in the runner) so every RunConfig
	// construction site — including the persistence/restart flows — gets it
	// from the single place all of them already pass through.
	if cfg.TestCase.UsesVault() && cfg.VaultTLSHost == "" {
		vp, err := PrepareVault(cfg.TmpDir, cfg.TestCase.Vault)
		if err != nil {
			return nil, fmt.Errorf("preparing vault topology: %w", err)
		}
		cfg.VaultTLSHost, cfg.VaultSecretsHost, cfg.VaultSeeds = vp.TLSDir, vp.SecretsDir, vp.Seeds
	}

	// Kerberos topology prep, same pattern as Vault: a gssapi case needs the
	// KDC bootstrap command + the shared krb5 dir before the compose file is
	// rendered.
	if cfg.TestCase.Kafka != nil && cfg.TestCase.Kafka.UsesGSSAPI() && cfg.KrbHostDir == "" {
		kp, err := PrepareKerberos(cfg.TmpDir, cfg.TestCase.Kafka)
		if err != nil {
			return nil, fmt.Errorf("preparing kerberos topology: %w", err)
		}
		cfg.KrbHostDir, cfg.KerberosInitCmd = kp.Dir, kp.InitCmd
	}

	composeFile := filepath.Join(cfg.TmpDir, "docker-compose.yaml")
	if err := writeCompose(composeFile, cfg); err != nil {
		return nil, err
	}

	cr := &ComposeRunner{cfg: cfg, composeFile: composeFile}
	cr.populateServiceNames()
	return cr, nil
}

// populateServiceNames fills in the cached generator/receiver service and
// container names so subsequent operations don't re-walk the case.
func (r *ComposeRunner) populateServiceNames() {
	tc := r.cfg.TestCase
	if tc.MultiGenerator() {
		for _, g := range tc.Generators {
			r.genServices = append(r.genServices, "generator-"+g.ID)
			r.genContainers = append(r.genContainers, "bench-generator-"+g.ID)
		}
	} else if tc.HasGenerator() {
		r.genServices = []string{"generator"}
		r.genContainers = []string{"bench-generator"}
	}
	// else: no generator configured — leave the lists empty so nothing waits
	// on or queries a generator container that the compose never renders.
	r.recvHostPorts = map[string]int{}
	// Verifier cases render no receiver service (the subject's sink is S3), so
	// leave the receiver metadata empty rather than advertising a receiver that
	// doesn't exist — ReceiverMetricsPort(s) must not point at a phantom port.
	if tc.UsesVerifier() {
		return
	}
	if tc.MultiReceiver() {
		for i, rc := range tc.Receivers {
			r.recvServices = append(r.recvServices, "receiver-"+rc.ID)
			r.recvContainers = append(r.recvContainers, "bench-receiver-"+rc.ID)
			r.recvHostPorts[rc.ID] = r.cfg.ReceiverHostPort + i
		}
	} else {
		r.recvServices = []string{"receiver"}
		r.recvContainers = []string{"bench-receiver"}
		r.recvHostPorts["default"] = r.cfg.ReceiverHostPort
	}
}

// dockerSocketGID is defined per-platform (docker_socket_gid_*.go). It returns
// the numeric gid owning /var/run/docker.sock, or "" if it can't be determined
// (e.g. Windows/macOS Docker Desktop). The collector image runs as a hardened
// non-root uid and needs to be in the docker group to read the socket over its
// HTTP API — the value is passed to docker-compose via `group_add`.

// Up starts all services detached.
func (r *ComposeRunner) Up() error {
	removeStaleBenchContainers()
	return r.compose("up", "-d", "--quiet-pull")
}

// removeStaleBenchContainers force-removes leftover bench-* containers from a
// previous run. Container names are fixed (bench-receiver, bench-localstack,
// …), so a teardown that lost the race — or a --no-cleanup debug run — makes
// every subsequent `compose up` fail with a name conflict. Sweeping by name
// prefix right before up keeps back-to-back subject runs from tripping over
// each other's corpses.
func removeStaleBenchContainers() {
	out, err := exec.Command("docker", "ps", "-aq", "--filter", "name=^bench-").Output()
	if err != nil {
		return
	}
	ids := strings.Fields(string(out))
	if len(ids) == 0 {
		return
	}
	fmt.Printf("  removing %d stale bench container(s)…\n", len(ids))
	args := append([]string{"rm", "-f"}, ids...)
	_ = exec.Command("docker", args...).Run()
}

// UpServices starts only the named compose services.
func (r *ComposeRunner) UpServices(services ...string) error {
	resolved := r.resolveServiceAliases(services)
	args := append([]string{"up", "-d", "--quiet-pull"}, resolved...)
	return r.compose(args...)
}

// resolveServiceAliases expands the logical names "generator" and "receiver"
// to the concrete plural service names when the case uses the plural form.
// Other names are passed through unchanged. Lets existing callers like
// `UpServices("generator")` keep working even after a case adopts plural
// generators.
func (r *ComposeRunner) resolveServiceAliases(names []string) []string {
	tc := r.cfg.TestCase
	out := make([]string, 0, len(names))
	for _, n := range names {
		switch n {
		case "generator":
			if tc.MultiGenerator() {
				out = append(out, r.genServices...)
				continue
			}
		case "receiver":
			if tc.MultiReceiver() {
				out = append(out, r.recvServices...)
				continue
			}
		}
		out = append(out, n)
	}
	return out
}

// StopServices sends SIGTERM to named services with the given grace timeout
// before SIGKILL. Uses `docker compose stop -t <seconds> <svc>...`.
func (r *ComposeRunner) StopServices(timeout time.Duration, services ...string) error {
	secs := max(int(timeout.Seconds()), 0)
	resolved := r.resolveServiceAliases(services)
	args := append([]string{"stop", "-t", strconv.Itoa(secs)}, resolved...)
	return r.compose(args...)
}

// KillServices sends SIGKILL immediately — no grace period, no chance for
// the process to clean up. Mirrors a crash scenario for persistence tests.
// Uses `docker compose kill -s SIGKILL <svc>...`.
func (r *ComposeRunner) KillServices(services ...string) error {
	resolved := r.resolveServiceAliases(services)
	args := append([]string{"kill", "-s", "SIGKILL"}, resolved...)
	return r.compose(args...)
}

// Down stops and removes all containers and anonymous volumes.
func (r *ComposeRunner) Down() error {
	return r.compose("down", "-v", "--remove-orphans")
}

// WaitForGeneratorExit blocks until ALL generator containers exit or the
// timeout expires. In the singular case there's exactly one; in the plural
// case every generator must finish before the run is considered "send-
// complete". Returns the first non-zero exit code observed (if any).
func (r *ComposeRunner) WaitForGeneratorExit(timeout time.Duration) error {
	deadline := time.Now().Add(timeout)
	pending := append([]string(nil), r.genContainers...)
	var firstErr error
	for time.Now().Before(deadline) {
		var still []string
		for _, name := range pending {
			out, err := exec.Command("docker", "inspect", "--format={{.State.Status}}", name).Output()
			if err != nil {
				still = append(still, name)
				continue
			}
			status := strings.TrimSpace(string(out))
			if status == "exited" {
				if cerr := r.checkExitCode(name); cerr != nil && firstErr == nil {
					firstErr = cerr
				}
				continue
			}
			still = append(still, name)
		}
		if len(still) == 0 {
			return firstErr
		}
		pending = still
		time.Sleep(2 * time.Second)
	}
	return fmt.Errorf("generator(s) did not exit within %s (still running: %v)", timeout, pending)
}

// WaitForVerifierExit blocks until the bench-verifier container exits or the
// timeout expires. A non-zero exit is NOT treated as an error here: the
// verifier exits 1 when the correctness verdict fails, which is a legitimate
// test outcome the runner reads from verdict.json — only a timeout (the
// container never finished) is an error.
func (r *ComposeRunner) WaitForVerifierExit(timeout time.Duration) error {
	const name = "bench-verifier"
	deadline := time.Now().Add(timeout)
	for time.Now().Before(deadline) {
		out, err := exec.Command("docker", "inspect", "--format={{.State.Status}}", name).Output()
		if err == nil && strings.TrimSpace(string(out)) == "exited" {
			return nil
		}
		time.Sleep(2 * time.Second)
	}
	return fmt.Errorf("verifier did not exit within %s", timeout)
}

func (r *ComposeRunner) checkExitCode(container string) error {
	out, err := exec.Command("docker", "inspect", "--format={{.State.ExitCode}}", container).Output()
	if err != nil {
		return nil
	}
	code := strings.TrimSpace(string(out))
	if code != "0" {
		logs, _ := exec.Command("docker", "logs", "--tail", "50", container).CombinedOutput()
		return fmt.Errorf("%s exited with code %s:\n%s", container, code, string(logs))
	}
	return nil
}

// Logs returns the last N lines of logs for a compose service.
func (r *ComposeRunner) Logs(service string, lines int) string {
	out, err := exec.Command("docker", "compose", "-f", r.composeFile,
		"logs", "--tail", fmt.Sprintf("%d", lines), "--no-color", service).CombinedOutput()
	if err != nil {
		return fmt.Sprintf("(could not get logs: %v)", err)
	}
	return string(out)
}

// StopCollector sends SIGTERM to the collector container and waits for it to exit.
// The collector writes its CSV file on SIGTERM, so this must be called before CopyMetricsCSV.
func (r *ComposeRunner) StopCollector() error {
	// docker stop sends SIGTERM and waits up to 10s for graceful exit.
	out, err := exec.Command("docker", "stop", "-t", "10", "bench-collector").CombinedOutput()
	if err != nil {
		return fmt.Errorf("docker stop bench-collector: %w\n%s", err, out)
	}
	return nil
}

// CopyMetricsCSV copies the metrics CSV from the (stopped) collector container to dst.
// Call StopCollector first so the CSV has been flushed.
func (r *ComposeRunner) CopyMetricsCSV(dst string) error {
	src := "bench-collector:/results/metrics.csv"
	out, err := exec.Command("docker", "cp", src, dst).CombinedOutput()
	if err == nil {
		return nil
	}
	// Enrich the error when the collector crashed before writing — otherwise
	// the user just sees "Could not find the file" with no clue why.
	stateOut, _ := exec.Command("docker", "inspect",
		"--format={{.State.Status}}|{{.State.ExitCode}}|{{.State.Error}}",
		"bench-collector").Output()
	state := strings.TrimSpace(string(stateOut))
	if state != "" && !strings.HasPrefix(state, "running|") {
		logs, _ := exec.Command("docker", "logs", "--tail", "30", "bench-collector").CombinedOutput()
		return fmt.Errorf("docker cp %s → %s: %w\ncollector state: %s\ncollector logs:\n%s\ndocker cp stderr:\n%s",
			src, dst, err, state, string(logs), out)
	}
	return fmt.Errorf("docker cp %s → %s: %w\n%s", src, dst, err, out)
}

// SubjectContainer returns the container name used for the subject.
func (r *ComposeRunner) SubjectContainer() string {
	return "bench-subject-" + r.cfg.Subject.Name
}

// ReceiverMetricsPort returns the host port where the receiver's /metrics
// endpoint is exposed. In the multi-receiver case this returns the first
// receiver's port — call ReceiverMetricsPorts() instead when each receiver
// is queried individually.
func (r *ComposeRunner) ReceiverMetricsPort() (int, func(), error) {
	return r.cfg.ReceiverHostPort, func() {}, nil
}

// ReceiverMetricsPorts returns the per-receiver host port map (id → port).
// For singular-receiver cases the map has one entry under id "default".
func (r *ComposeRunner) ReceiverMetricsPorts() map[string]int {
	out := make(map[string]int, len(r.recvHostPorts))
	maps.Copy(out, r.recvHostPorts)
	return out
}

// GeneratorContainers returns the names of all generator containers used
// for this run (one in singular mode, N in plural mode). Used by the
// runner when it needs to read each generator's stdout.
func (r *ComposeRunner) GeneratorContainers() []string {
	return append([]string(nil), r.genContainers...)
}

// GeneratorStdout returns the stdout of the (singular) generator container.
// In plural-generator mode this returns the concatenation of every
// generator's stdout, separated by newlines, so callers that aggregate by
// summing fields across JSON objects still get every blob.
func (r *ComposeRunner) GeneratorStdout() string {
	if len(r.genContainers) == 1 {
		out, _ := exec.Command("docker", "logs", r.genContainers[0]).Output()
		return string(out)
	}
	var b strings.Builder
	for _, c := range r.genContainers {
		out, _ := exec.Command("docker", "logs", c).Output()
		b.Write(out)
		b.WriteString("\n")
	}
	return b.String()
}

func (r *ComposeRunner) compose(args ...string) error {
	cmdArgs := append([]string{"compose", "-f", r.composeFile}, args...)
	cmd := exec.Command("docker", cmdArgs...)
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	return cmd.Run()
}

