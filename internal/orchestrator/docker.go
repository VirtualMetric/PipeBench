// resolveGeneratorConnections picks the generator's parallel-connection
// count. An explicit non-zero value in case.yaml always wins. When unset
// (0), scale with the host's CPU count: ~1 connection per 4 cores with a
// floor of 4 so small dev machines aren't degenerate. On a 192-core
// c6a.metal this produces 48 connections; on a 32-core box, 8.
//
// The intent is "what the host can drive", not "what 4 connections do" —
// makes per-tier results comparable on similar hardware while letting
// each tier exercise its actual capacity.
func resolveGeneratorConnections(explicit int) int {
	if explicit > 0 {
		return explicit
	}
	if n := runtime.NumCPU() / 4; n > 4 {
		return n
	}
	return 4
}

